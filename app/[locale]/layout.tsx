import { Metadata } from "next";
import { NextIntlClientProvider, useMessages } from "next-intl";
import { Cairo } from "next/font/google";
import Header from "../_components/Header";
import { UserProvider } from "../_context/UserContext";
import useTextDirection from "../_hooks/useTextDirection";
import "./globals.css";
import Footer from "../_components/Footer";
import { NextUIProvider } from "@nextui-org/react";

const cairo = Cairo({
  weight: ["300", "400", "500", "700"],
  subsets: ["latin"],
  display: "swap",
  adjustFontFallback: false,
});

export async function generateMetadata({
  params,
}: {
  params: { locale: string };
}): Promise<Metadata> {
  const locale = params.locale || "ar";
  const title = locale === "ar" ? "كريبتو حلال" : "Crypto Halal";
  const description = "Generated by create next app";
  return {
    title,
    description,
  };
}

export default function RootLayout({
  children,
  params: { locale },
}: Readonly<{
  children: React.ReactNode;
  params: { locale: string };
}>) {
  const dir = useTextDirection();
  const messages = useMessages();

  return (
    <html lang={locale} dir={dir}>
      <body className={cairo.className} suppressHydrationWarning={true}>
        <UserProvider>
          <NextUIProvider>
            <NextIntlClientProvider messages={messages}>
              <Header />
              <div className="mt-[100px]">{children}</div>
              <Footer />
            </NextIntlClientProvider>
          </NextUIProvider>
        </UserProvider>
      </body>
    </html>
  );
}
